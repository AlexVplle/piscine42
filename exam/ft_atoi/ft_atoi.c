#include <stdio.h>

int	ft_atoi(const char *str)
{
	int				negative;
	unsigned int	num;	

	negative = 1;
	num = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			negative = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + *str - '0';
		str++;
	}
	return ((int) negative * num);
}

int	main(int argc, char *argv[])
{
	(void) argc;
	printf("%d\n", ft_atoi(argv[1]));
}
