int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] && str[i] >= 'A' && str[i] <= 'Z')
	{
		str[i] += 32;
		i++;
	}
	i = 0;
	while (str[i])
	{
		if (str[i] < 'a' || str[i] > 'z')
			return (0);
		i++;
	}
	return (1);
}
